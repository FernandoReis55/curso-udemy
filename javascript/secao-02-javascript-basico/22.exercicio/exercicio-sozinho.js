// Pedir usuario para digitar 2 numeros
// Converter string em number
// Fazer a soma dos numeros digitados
// Criar alert com o resultado

// caixa com pedido de numero
let num1 = prompt('Digite um número!');
let num2 = prompt('Digite outro número!');

// converção de string para Number
num1 = Number(num1);
num2 = Number(num2);

// exibição do resultado
alert(`O Resultado foi: ${num1 + num2}`);

